let searchIcon = document.querySelectorAll('.search-icon')
let siteSearch = document.querySelectorAll('.site-search')
let searchWord = document.querySelector('.searchWord')

// Search 

searchIcon.forEach((a) => {
  a.addEventListener('click', () => {
    siteSearch.forEach((r) => {
      let userData = r.value
      if (userData) {
        let data = products.filter((e) =>
          e.title.toLowerCase().includes(userData.toLowerCase()),
        )
        localStorage.setItem('search', JSON.stringify(data))
        location.href = 'search.html'
      }
    })
  })
})


